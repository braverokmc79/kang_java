package first;

public class PrimitiveDataTypeExam {
	
	public static void main(String[] args) {
		//논리형 리터럴
		boolean isFun =false;
		System.out.println(isFun);
		
		//문자 리터럴
		char c='f';
		
		//정수리터럴
		int x=59;

		//큰 정수리터럴
		long bing =345564546L;
		
		//실수리터럴
		float f =32.4f;
		
		//큰 실수 리터럴
		double d=343434.5;
		
	}
}



